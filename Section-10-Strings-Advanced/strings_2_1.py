
greetings = 'Welcome, to the Python world!'

# Indexing
print(greetings[0])

# Slicing
print(greetings[0:7])
print(greetings[0:7:2])

print(len(greetings))

# returns list that is separated by spaces
print(greetings.split())
# returns list separated by the argument
print(greetings.split(','))

# finds and returns the index
print(greetings.find('Python'))
